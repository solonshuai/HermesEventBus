package com.zxy.mytsfqxproject.mvp.entity;

public class PhoneList {
    private String name;
    private String tel;
    private int id;


    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getNumber() {
        return tel;
    }

    public void setNumber(String number) {
        this.tel = number;
    }
}
